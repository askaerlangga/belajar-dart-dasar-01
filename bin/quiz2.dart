void main(){
  //Keliling persegi
  int sisiPersegi = 10;
  int kelilingPersegi = sisiPersegi * 4;
  print(kelilingPersegi);

  //Luas lingkaran
  int jariJariLingkaran = 10;
  double luasLingkaran = 3.14 * (jariJariLingkaran * jariJariLingkaran);
  print(luasLingkaran);

  //Volume balok
  int panjangBalok = 6;
  int tinggiBalok = 8;
  int lebarBalok = 10;
  int volumeBalok = panjangBalok * tinggiBalok * lebarBalok;
  print(volumeBalok);
}