void main(){
  var angka1 = 10;
  var angka2 = 5;

  //Penjumlahan
  print(angka1 + angka2);

  //Pengurangan
  print(angka1 - angka2);

  //Perkalian
  print(angka1 * angka2);

  //Pembagian
  print(angka1 / angka2);

  //Pembagian yang dibulatkan
  print(angka1 ~/ angka2);

  //Modulo atau Sisa Bagi
  print(angka1 % angka2);

  //Angka negatif (-angka)
  print(-angka1 + angka2);

}