void main(){
  //Menghasilkan Boolean, true atau false

  //Angka
  var angka1 = 10;
  var angka2 = 5;

  //Sama dengan
  print(angka1 == angka2);
  //Tidak sama dengan
  print(angka1 != angka2);
  //Lebih dari
  print(angka1 > angka2);
  //Kurang dari
  print(angka1 < angka2);
  //Lebih dari sama dengan
  print(angka1 >= angka2);
  //Kurang dari sama dengan
  print(angka1 <= angka2);

  //String
  print('aska' == 'aska');
  print('aska' != 'aska');
}